// To Check Whether Number is Even or Odd 
#include<iostream> //header file
using namespace std;

int main(){
	int n; //variables
	cout<<"Enter a number:";
	cin>>n;
//conditional statement	
	if(n%2==0)                             
	{
		cout<<"The number is: even"<<endl;
	}
	else
	{
		cout<<"The number is: odd"<<endl;
	}

return 0;
}
